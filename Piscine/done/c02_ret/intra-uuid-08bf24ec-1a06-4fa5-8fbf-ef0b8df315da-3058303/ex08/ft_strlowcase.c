char	*ft_strlowcase(char *str)
{
	char	*chg;

	chg = str;
	while (*chg != 0)
	{
		if ((*chg >= 65 && *chg <= 90))
		{
			*chg = *chg + 32;
		}
		chg++;
	}
	return (str);
}
