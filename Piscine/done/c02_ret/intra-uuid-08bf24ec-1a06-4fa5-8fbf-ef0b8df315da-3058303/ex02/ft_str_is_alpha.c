int		ft_str_is_alpha(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if ((*str < 65) || (90 < *str && *str < 97) || (122 < *str))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
