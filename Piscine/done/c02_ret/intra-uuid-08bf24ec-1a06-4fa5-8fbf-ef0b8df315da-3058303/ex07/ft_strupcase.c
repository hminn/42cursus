char	*ft_strupcase(char *str)
{
	char	*chg;

	chg = str;
	while (*chg != '\0')
	{
		if ((*chg >= 97) && (*chg <= 122))
		{
			*chg = *chg - 32;
		}
		chg++;
	}
	return (str);
}
