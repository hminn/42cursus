int		ft_sqrt(int nb)
{
	long long i;

	i = 0;
	if (nb < 0)
		return (0);
	else if (nb == 1)
		return (1);
	else
	{
		while ((i * i) <= nb)
		{
			if (nb == (i * i))
				return (i);
			i++;
		}
	}
	return (0);
}
